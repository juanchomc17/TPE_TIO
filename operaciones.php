<?php 


function restar($v1,$v2){
	$resultado =  $v1 - $v2;
	return "El resultado es: " . $resultado;
}

function sumar($v1,$v2){
	$resultado = $v1 + $v2;
	return "El resultado es: " . $resultado;	
}

function multiplicar($v1,$v2){
	$resultado = $v1 * $v2;
	return "El resultado es: " . $resultado;
}

function dividir($v1,$v2){
	$resultado = $v1 / $v2;
	return "El resultado es: " . $resultado;
}



 ?>